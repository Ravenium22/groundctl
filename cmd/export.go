var exportCmd = &cobra.Command{
	Use:   "export",
	Short: "Export environment spec in various formats",
	Long: `Exports the .ground.yaml tool list for use in other contexts.

Supported formats:
  dockerfile   Dockerfile ENV/RUN stanzas for installing tools
  shell        Shell export statements (source-able)
  json         JSON tool list with detected versions`,
	RunE: runExport,
}

var (
	exportFormat string
	exportOutput string
	exportConfig string
)

func init() {
	exportCmd.Flags().StringVarP(&exportFormat, "format", "f", "dockerfile", "Output format: dockerfile, shell, json")
	exportCmd.Flags().StringVarP(&exportOutput, "output", "o", "", "Write to file (default: stdout)")
	exportCmd.Flags().StringVarP(&exportConfig, "config", "c", "", "Path to .ground.yaml")
	rootCmd.AddCommand(exportCmd)
}

func runExport(cmd *cobra.Command, args []string) error {
	configPath := exportConfig
	if configPath == "" {
		configPath = findConfig()
	}
	if configPath == "" {
		configPath = filepath.Join(".", config.DefaultConfigFile)
	}

	cfg, err := config.Load(configPath)
	if err != nil {
		return err
	}

	// Detect current versions for context
	names := make([]string, len(cfg.Tools))
	for i, t := range cfg.Tools {
		names[i] = t.Name
	}
	detected := detector.DetectByNames(names)
	detectedMap := make(map[string]model.DetectedTool)
	for _, d := range detected {
		detectedMap[d.Name] = d
	}

	var output string
	switch exportFormat {
	case "dockerfile", "docker":
		output = exportDockerfile(cfg, detectedMap)
	case "shell", "sh":
		output = exportShell(cfg, detectedMap)
	case "json":
		data, err := json.MarshalIndent(struct {
			Name  string             `json:"name"`
			Tools []model.DetectedTool `json:"tools"`
		}{
			Name:  cfg.Name,
			Tools: detected,
		}, "", "  ")
		if err != nil {
			return err
		}
		output = string(data) + "\n"
	default:
		return fmt.Errorf("unsupported format %q (use: dockerfile, shell, json)", exportFormat)
	}

	if exportOutput != "" {
		if err := os.MkdirAll(filepath.Dir(exportOutput), 0755); err != nil {
			return fmt.Errorf("could not create output directory: %w", err)
		}
		if err := os.WriteFile(exportOutput, []byte(output), 0644); err != nil {
			return fmt.Errorf("could not write export: %w", err)
		}
		fmt.Printf("Export written to %s\n", exportOutput)
	} else {
		fmt.Print(output)
	}

	return nil
}

