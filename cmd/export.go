func exportDockerfile(cfg *model.GroundConfig, detected map[string]model.DetectedTool) string {
	var b strings.Builder

	b.WriteString(fmt.Sprintf("# Generated by groundctl from %s\n", cfg.Name))
	b.WriteString("# Add to your Dockerfile\n\n")

	// ENV stanza with version info
	b.WriteString("# Tool versions (from .ground.yaml)\n")
	for _, tool := range cfg.Tools {
		ver := tool.Version
		if d, ok := detected[tool.Name]; ok && d.Found {
			ver = d.Version
		}
		envName := strings.ToUpper(strings.ReplaceAll(tool.Name, "-", "_")) + "_VERSION"
		b.WriteString(fmt.Sprintf("ENV %s=%s\n", envName, ver))
	}

	b.WriteString("\n# Install tools\n")
	for _, tool := range cfg.Tools {
		if cmd, ok := dockerToolInstall[tool.Name]; ok {
			b.WriteString(fmt.Sprintf("RUN %s\n", cmd))
		} else {
			b.WriteString(fmt.Sprintf("# TODO: install %s manually\n", tool.Name))
		}
	}

	return b.String()
}

func exportShell(cfg *model.GroundConfig, detected map[string]model.DetectedTool) string {
	var b strings.Builder

	b.WriteString(fmt.Sprintf("# Generated by groundctl from %s\n", cfg.Name))
	b.WriteString("# Source this file: source <(ground export -f shell)\n\n")

	for _, tool := range cfg.Tools {
		envName := "GROUND_" + strings.ToUpper(strings.ReplaceAll(tool.Name, "-", "_")) + "_VERSION"
		constraint := tool.Version
		b.WriteString(fmt.Sprintf("export %s=%q\n", envName, constraint))

		if d, ok := detected[tool.Name]; ok && d.Found {
			actualName := "GROUND_" + strings.ToUpper(strings.ReplaceAll(tool.Name, "-", "_")) + "_ACTUAL"
			b.WriteString(fmt.Sprintf("export %s=%q\n", actualName, d.Version))
		}
	}

	return b.String()
}
